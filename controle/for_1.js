//para o laço FOR, vamos ter três partes 1) a declaracao da variavel que coloca-se dentro do ciclo; 2) a expressão; 3) o incrimento ou a modificação 
// O FOR É UTILIZADO PARA UMA QUANTIDADE DETERMINADA DE REPETIÇÕES

for (let controle = 1; controle <= 10; controle += 2) { //estou a incrementar mais 2
    console.log(controle);
}
console.log('Fim')